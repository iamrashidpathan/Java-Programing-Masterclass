package academy.learnprograming;

public class Resolution {

    //fields
    private int width;
    private int height;

    //Constructors
    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    //getters
    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
